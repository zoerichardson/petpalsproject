using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using PetPals.Data;
using PetPals.Models;

namespace PetPals.Pages.Services
{
    [Authorize(Policy = "SitterProfile")]
    public class DeleteModel : PageModel
    {
        private readonly PetPals.Data.ApplicationDbContext _context;

        public DeleteModel(PetPals.Data.ApplicationDbContext context)
        {
            _context = context;
        }

        [BindProperty]
        public Service Service { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }
            //returns the service to be delete by looping through the database to match it will the ID passed through the parameter
            Service = await _context.Service
                .Include(s => s.Sitter).FirstOrDefaultAsync(m => m.ServiceID == id);

            if (Service == null)
            {
                return NotFound();
            }
            return Page();
        }

        public async Task<IActionResult> OnPostAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }
            //finds the service in the database by looking for the ID passed through the parameter and removes in and saves the database
            Service = await _context.Service.FindAsync(id);

            if (Service != null)
            {
                _context.Service.Remove(Service);
                await _context.SaveChangesAsync();
            }

            return RedirectToPage("./Index");
        }
    }
}
